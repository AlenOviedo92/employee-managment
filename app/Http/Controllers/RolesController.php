    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|unique:roles|max:255'
        ]);
        $role = new Role;
        $role->name = $request->name;
        $role->save();

        return redirect()->route('roles.index')->with('success', 'Nuevo rol agregado');
    }

